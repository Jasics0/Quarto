    public void fillReport1() {
        modelo = new DefaultTableModel();
        modelo.addColumn("Reporte 1");
        this.tabla.setModel(modelo);
        String r1[] = s.report1();
        for (int i = 0; i < r1.length; i++) {
            String info[] = new String[1];
            info[0] = r1[i];
            modelo.addRow(info);
        }

    }

