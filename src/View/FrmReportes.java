    public void fillReport3() {
        String r3[] = s.report3();
        modelo = new DefaultTableModel();
        modelo.addColumn("# Partida");
        modelo.addColumn("Tiempo");
        this.tabla3.setModel(modelo);
        String aux[];
        String info[] = new String[2];
        for (int i = 0; i < r3.length; i++) {
            aux = r3[i].split(",");
            info[0] = aux[0];
            info[1] = aux[3];
            modelo.addRow(info);
        }
    }

